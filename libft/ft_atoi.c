#include"libft.h"

int	ft_atoi(const char *ptr)
{
	int	num;
	int	sign;

	sign = 1;
	num = 0;
	while ((*ptr == ' ') || (*ptr >= 9 && *ptr <= 13))
		ptr++;
	if ((*ptr == '-') || (*ptr == '+'))
	{
		if (*ptr == '-')
			sign *= -1;
		ptr++;
	}
	while ((*ptr >= '0') && (*ptr <= '9'))
	{
		num *= 10;
		num += (*ptr - 48);
		ptr++;
	}
	return (num * sign);
}
